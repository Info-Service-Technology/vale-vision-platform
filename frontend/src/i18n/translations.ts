export function normalizeLang(lang?: string | null): Lang {
  const value = (lang || "").trim();

  if (supportedLanguages.includes(value as Lang)) {
    return value as Lang;
  }

  const lower = value.toLowerCase();

  if (lower === "pt" || lower.startsWith("pt-br")) return "pt-BR";
  if (lower.startsWith("pt-pt")) return "pt-PT";
  if (lower === "en" || lower.startsWith("en-")) return "en";
  if (lower === "es" || lower.startsWith("es-")) return "es";
  if (lower === "fr" || lower.startsWith("fr-")) return "fr";
  if (lower === "de" || lower.startsWith("de-")) return "de";
  if (
    lower === "zh" ||
    lower.startsWith("zh-") ||
    lower.startsWith("zh_cn") ||
    lower.startsWith("zh-hans")
  ) {
    return "zh-CN";
  }

  return "pt-BR";
}
